export interface ISteelSection {
  designation: string;
  type: 'ISMB' | 'ISWB' | 'ISLB' | 'ISHB' | 'ISMC' | 'ISSC' | 'ISA' | 'ISNT' | 'CHS' | 'RHS' | 'SHS';
  
  // Dimensions (mm)
  depth: number;          // D - Overall depth
  width: number;          // B - Flange width
  tw: number;             // tw - Web thickness
  tf: number;             // tf - Flange thickness
  r1?: number;            // r1 - Root radius (fillet)
  r2?: number;            // r2 - Toe radius
  
  // Section Properties
  area: number;           // A - Cross-sectional area (mm²)
  mass: number;           // w - Mass per meter (kg/m)
  
  // Second Moment of Area (mm⁴)
  Ixx: number;            // About major axis
  Iyy: number;            // About minor axis
  
  // Section Modulus (mm³)
  Zxx: number;            // Elastic - major axis
  Zyy: number;            // Elastic - minor axis
  Zpxx?: number;          // Plastic - major axis
  Zpyy?: number;          // Plastic - minor axis
  
  // Radius of Gyration (mm)
  rxx: number;            // About major axis
  ryy: number;            // About minor axis
  
  // Additional
  Cw?: number;            // Warping constant (mm⁶)
  J?: number;             // Torsional constant (mm⁴)
}

