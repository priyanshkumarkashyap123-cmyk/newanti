/** Section type filter options */
export type SectionType = 'ISMB' | 'ISMC' | 'ISA' | 'SHS' | 'RHS' | 'CHS';

/** Filter criteria for section search */
export interface SectionFilter {
  minIxx?: number;   // mm⁴
  minZxx?: number;   // mm³
  maxMass?: number;  // kg/m
  minDepth?: number; // mm
  maxDepth?: number; // mm
}

/**
 * Get all sections of a specific type
 */
export function getSectionsByType(type: SectionType): ISteelSection[] {
  const allSections: Record<SectionType, ISteelSection[]> = {
    ISMB: Object.values(ISMB_SECTIONS),
    ISMC: Object.values(ISMC_SECTIONS),
    ISA: Object.values(ISA_EQUAL_SECTIONS),
    SHS: Object.values(SHS_SECTIONS),
    RHS: Object.values(RHS_SECTIONS),
    CHS: Object.values(CHS_SECTIONS),
  };
  
  return allSections[type] || [];
}

/**
 * Get section by designation
 */
export function getSectionByDesignation(designation: string): ISteelSection | undefined {
  const allSections = {
    ...ISMB_SECTIONS,
    ...ISMC_SECTIONS,
    ...ISA_EQUAL_SECTIONS,
    ...SHS_SECTIONS,
    ...RHS_SECTIONS,
    ...CHS_SECTIONS,
  };
  
  return allSections[designation];
}

/**
 * Find optimal section for given requirements
 */
export function findOptimalSection(
  type: SectionType,
  criteria: SectionFilter
): ISteelSection[] {
  const sections = getSectionsByType(type);
  
  return sections.filter(section => {
    if (criteria.minIxx && section.Ixx < criteria.minIxx) return false;
    if (criteria.minZxx && section.Zxx < criteria.minZxx) return false;
    if (criteria.maxMass && section.mass > criteria.maxMass) return false;
    if (criteria.minDepth && section.depth < criteria.minDepth) return false;
    if (criteria.maxDepth && section.depth > criteria.maxDepth) return false;
    return true;
  }).sort((a, b) => a.mass - b.mass); // Sort by weight (lightest first)
}

/**
 * Calculate section utilization for bending
 */
export function calculateSectionUtilization(
  section: ISteelSection,
  Mu: number,          // kN·m - Design moment
  fy: number,          // MPa - Yield strength
  gamma_m0: number = 1.10 // Partial safety factor
): { utilization: number; Md: number; status: string } {
  // Design bending strength (assuming compact section)
  const Zp = section.Zpxx || section.Zxx * 1.15; // Approximate plastic modulus
  const Md = (Zp * fy / gamma_m0) / 1e6; // kN·m
  
  const utilization = Mu / Md;
  
  return {
    utilization: Math.round(utilization * 1000) / 1000,
    Md: Math.round(Md * 100) / 100,
    status: utilization <= 1.0 ? 'OK' : 'OVERSTRESSED',
  };
}

